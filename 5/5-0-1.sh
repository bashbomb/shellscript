#!/bin/bash
set -euo pipefail

red=$(tput setaf 1)
green=$(tput setaf 2)
yellow=$(tput setaf 3)
reset=$(tput sgr0)

clear
cat << EOF

5. 간단 쉘 스크립트
   - 비교표

     +-------------+-------------------------+-------------+----------------------------------+-------------+---------------------------------+
     | ${green}숫자 비교문${reset} |           설명          | ${green}문자 비교문${reset} |               설명               | ${green}파일 비교문${reset} |               설명              |
     +=============+=========================+=============+==================================+=============+=================================+
     |   A -eq B   | A와 B가 같은가          |    A = B    | 문자 A, B가 같은가               |   -d file   | 파일이 존재하고 디렉토리인가    |
     +-------------+-------------------------+-------------+----------------------------------+-------------+---------------------------------+
     |   A -ge B   | A가 B보다 크거나 같은가 |    A != B   | 문자 A, B가 다른가               |   -e file   | 파일이 존재하는가               |
     +-------------+-------------------------+-------------+----------------------------------+-------------+---------------------------------+
     |   A -gt B   | A가 B보다 큰가          |    A < B    | 문자열 A가 B보다 작은가 (바이트) |   -f file   | 파일이 존재하고 파일인가        |
     +-------------+-------------------------+-------------+----------------------------------+-------------+---------------------------------+
     |   A -le B   | A가 B보다 작거나 같은가 |    A > B    | 문자열 A가 B보다 큰가 (바이트)   |   -r file   | 파일이 존재하고 읽을 수 있는가  |
     +-------------+-------------------------+-------------+----------------------------------+-------------+---------------------------------+
     |   A -lt B   | A가 B보다 작은가        |     -n A    | A의 문자열 길이가 0보다 큰가     |   -s file   | 파일이 존재하고 비어있지 않은가 |
     +-------------+-------------------------+-------------+----------------------------------+-------------+---------------------------------+
     |   A -ne B   | A와 B가 같지 않은가     |     -z A    | A의 문자열의 길이가 0인가        |   -w file   | 파일이 존재하고 쓰기가 가능한가 |
     +-------------+-------------------------+-------------+----------------------------------+-------------+---------------------------------+

EOF

echo -n "Next 조건문 y/n : "
read A
[ ${A} == y ] && ./5/5-1-0.sh || ./5/5-0-0.sh
