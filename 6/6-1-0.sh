      |    A|B   | A 또는 B가 있음을 의미           | c[A|B]f : cAf cBf      |
      +----------+----------------------------------+------------------------+
      |   [a-z]  | a부터 z까지 알파벳의 범위를 매칭 | a b c d e ......       |
      +----------+----------------------------------+------------------------+

EOF

echo -n "Next 정규표현식 y/n : "
